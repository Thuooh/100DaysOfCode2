# A refresher on python basic functions

# IF/Else
if (2 > 1) == (5 < 10):
  print('Both expressions give the same result')

c = '2'
d = 2

if c == d:
  print('They are equal')
else:
  print('They are not equal')

# Not equal 

val1 = 10
val2 = 20

if val1 != val2:
  print("They are NOT equal")

if (10 > 1) != (10 > 1000):
  print("They are NOT equal")
